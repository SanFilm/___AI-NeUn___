def del_from_tuple_1(tpl, elem):                            # Функция удаления элемента из кортежа
    if elem in tpl:                                         # Проверка, присутствует ли элемент в кортеже
        elem_index = tpl.index(elem)                        # Извлечение индекса заданного элемента
        tpl = tpl[:elem_index] + tpl[elem_index + 1:]       # Создание нового кортежа без указанного элемента 
    return tpl                                              # Возврат получившегося кортежа

# -F- ------------------------------------------------------

def del_from_tuple_2(tpl, elem):                            # Функция удаления элемента из кортежа
    lst = list(tpl)                                         # Преобразование кортежа в список
    if elem in tpl:                                         # Проверка, присутствует ли элемент в списке
        lst.remove(elem)                                    # Удаление элемента из списка
    return tuple(lst)                                       # Возврат обратно преобразованного в кортеж списка

